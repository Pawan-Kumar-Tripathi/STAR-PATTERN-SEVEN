import java.io.*;
import java.util.*;
class star_pattern7
    {
        public void main()
        
        {
            int n=7;
            for(int i =1;i<=n;i++)
            {
            for(int j=1;j<=i;j++)
            {
                System.out.print(j);
            }
            System.out.println(" ");
        }
    }
}
    
            